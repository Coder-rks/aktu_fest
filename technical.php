<h1>Marking and Winning Criteria</h1>
<p>Participants who have been able to implement maximum number of 

combinations from the truth table will be declared winner. In case of two or more than two participants 

end up tied, preference will be given to those who have used minimum number of gates. Gates created 

by making use of transistors will not be counted as gate (for the purpose of judging this event). In other 

words participants are encouraged to use maximum number of transistors to design their circuit. Even 

after the application of the above mentioned tie-breaker two or more than two teams end up tied, than 

the team clocking least time for completing the circuit design and implementation will be given 

    preference.</p>
 
