<center><h1><u><b>Autonomous Robot: Maze Runner</b></u></h1></center>
<p><h2>Objective</h2> The participants are expected to build a robot that should traverse a maze consisting of black 

lines printed on white flex sheet. The teams are expected to build the robot from scratch. However they 

    can use open source prototyping boards like Arduino and Raspberry Pi.</p>
<p>Arena description: The arena will consist of maze made up of black lines (30 mm wide) printed on white 

flex sheet (see figure). The maze is guaranteed to be without loops. The distance between any two 

    parallel lines will not be less than 200 mm.</p>
<h2>Description</h2> <p>Each robot will be given two runs. During the first run the robot should start from the black 

square (marked as start) and should ultimately reach the other black square (marked as end) covering 

each part of the maze. During the second run the robot should return to the starting point passing 

through the most direct route possible.
</p>
<div class="col-md-12">
<a href="images/maze.png" data-lighter>
<center><img class="img-responsive" src="images/maze.png" width="400" height="400"/></center></a>
</div>
<h2>Marking scheme and winning criteria</h2>
<p>During the first run, for each end point visited, the robot will be 

awarded 10 points and will be penalized by 5 points for missing any. It is to be noted that there are 17 

end points. During the second run the robot will be awarded 10 marks for crossing each junction falling 

in the most direct path and will be penalized by 10 points for touching any end point. It is to be noted 

that there are 7 junctions falling in the most direct path. The team having maximum score will be 

declared winner. If two teams are tied at the end of the game, the team clocking less time during the 

second run will be declared winner.
</p>
