    <h1> Description</h1>
<p>The designed logic circuit should be able to verify a truth table having up-to 16 input and 

output combinations. The truth table will be generated on the spot either randomly or in consultation 

with the participants. Please see appendix â€“ A for an illustrative problem. In addition following general 

    set of conditions are to be met:</p>
<ul>
    <li>Use of circuit simulation software is not allowed.</li>

<li> Participants should bring their own set of components (as mentioned above) and other tools 

    and accessories (breadboards and connector wires).</li>

<li>They are advised to carry each component in sufficient numbers so as to be able to design 

