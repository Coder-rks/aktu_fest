<center><h1><u><b>Manual Robot Competition: Road Rash</b></u></h1></center>
<h2>Objective</h2><p> Two manual robots will be racing against each other in a circular race track having some

minor obstacles. The winner will be decided upon the ability of the robot to outrun the opponent 

without falling off the edge of the arena.
</p>
<h2>Arena Description</h2>
<p>The arena will be a 400 mm wide circular racing track made up of ordinary plywood. 

The inner diameter of the track will be 4000 mm. Arena will be parallel to the ground but fixed at a 

height of 300 mm from it. It will be made up of ordinary plywood. There will be two obstacles located diametrically opposite to each other. The obstacles will be stainless steel rod (10 mm diameter) fixed at 

a height of 20 mm from the surface of the racing track
</p>

<h2>Description</h2><p> The robots will start from the starting positions (marked as A and B) and both of them will 

move in the same sense (shown by arrows). The robots are expected to chase and approach the other 

robot from behind. Any dimension of the robot should not be less than 200 mm during all time of the 

game.</p>
<div class="col-md-12">
<a href="images/6.png" data-lighter>
<center><img class="img-responsive" src="images/6.png" width="400" height="400"/></center></a>
</div>


<h2>Marking and Winning Criteria</h2> <p>There will be three rounds of 3 minutes each. Each round will end with 

the fulfillment of any one of the following conditions: </p>
<ul>
<li> A robot successfully chases its opponent and touches it from behind. The chaser will be declared 

winner of the round and will be awarded 10 points.
    </li>
<li>If a robot falls of the racing track. The opponent will be declared winner of the round and will be 

awarded 10 points.
    </li>
<li>The round will get over with the expiry of 3 minutes. None of the robots will be awarded any 

point.
    </li>

   <p> At the end of the three rounds robot scoring maximum points will be declared winner. In case both the 

robot end up tied the game may be extended by another round of 3 minutes. 

If the winner is undecided even by the end of the fourth round, the robot closest to its opponent 

(measured from head of the robot to the tail of the opponent and measured along the circular track) will 

        be declared winner.</p>
</ul><br /></div>
